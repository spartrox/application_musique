class Musique {
  //création de la class Musique et des variables que l'on va utiliser à l'intérieur
  String titre;
  String auteur;
  String image;
  String musiqueURL;

  Musique(String titre, String auteur, String image, String musiqueURL) {
    //Instancie la class Musique POO
    this.titre = titre;
    this.auteur = auteur;
    this.image = image;
    this.musiqueURL = musiqueURL;
  }
}
